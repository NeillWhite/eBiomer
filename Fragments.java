	private void centerResidue(){
		// the following centers the fragment
		float xCenter = 0.0f, yCenter = 0.0f, zCenter = 0.0f;
		for ( int i = 0; i < residue.numberOfAtoms; i++ ){
			xCenter += residue.atom[ i ].coord[ 0 ];
			yCenter += residue.atom[ i ].coord[ 1 ];
			zCenter += residue.atom[ i ].coord[ 2 ];
		}
		xCenter /= residue.numberOfAtoms;
		yCenter /= residue.numberOfAtoms;
		zCenter /= residue.numberOfAtoms;
		for ( int i = 0; i < residue.numberOfAtoms; i++ ){
			residue.atom[ i ].coord[ 0 ] -= xCenter;
			residue.atom[ i ].coord[ 1 ] -= yCenter;
			residue.atom[ i ].coord[ 2 ] -= zCenter;
		}
		// now update the actual coords
		int ix3 = -3;
		for ( int i = 0; i < residue.numberOfAtoms; i++ ){
			ix3 += 3;
			residue.actualCoordinates[ ix3 ] = residue.atom[ i ].coord[ 0 ];
			residue.actualCoordinates[ ix3 + 1 ] = residue.atom[ i ].coord[ 1 ];
			residue.actualCoordinates[ ix3 + 2 ] = residue.atom[ i ].coord[ 2 ];
			residue.atom[ i ].residueAtomNumber = i;
		}
	}

